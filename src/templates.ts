let currentLang = window.currentLang || 'fr';
let currentPageKey = window.currentPageKey || 'page1'; // Set by each individual HTML file

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    // Setup language dropdown toggle
    const langBtn = document.getElementById('langSelectBtn');
    const langDropdown = document.getElementById('langDropdown');

    if (langBtn && langDropdown) {
        langBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            langDropdown.classList.toggle('opacity-0');
            langDropdown.classList.toggle('invisible');
            langDropdown.classList.toggle('opacity-100');
            langDropdown.classList.toggle('visible');
        });

        // Close dropdown when clicking outside
        document.addEventListener('click', () => {
            langDropdown.classList.add('opacity-0', 'invisible');
            langDropdown.classList.remove('opacity-100', 'visible');
        });
    }

    // Apply initial translations
    applyTranslations();
});

// Change language globally
function setLanguage(lang) {
    currentLang = lang;
    applyTranslations();
}

// Function that maps translation dictionary to HTML elements via data-i18n attributes
function applyTranslations() {
    const t = translations[currentLang];
    const pageT = t[currentPageKey];

    // Update Language Button Text
    const langNameEl = document.getElementById('currentLangName');
    if (langNameEl) langNameEl.textContent = t.langName;

    // Update Page Content Data
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');

        if (pageT) {
            switch (key) {
                case 'title': el.textContent = pageT?.title || ''; break;
                case 'subtitle': el.textContent = pageT?.subtitle || ''; break;
                case 'cta':
                    const icon = el.querySelector('svg') || el.querySelector('i');
                    el.textContent = (pageT?.cta || '') + " ";
                    if (icon) el.appendChild(icon);
                    break;
                case 'benefit1': el.textContent = pageT?.benefits?.[0] || ''; break;
                case 'benefit2': el.textContent = pageT?.benefits?.[1] || ''; break;
                case 'benefit3': el.textContent = pageT?.benefits?.[2] || ''; break;
            }
        }

        switch (key) {
            // Footer translations
            case 'footerDisclaimer': el.innerHTML = t.footerDisclaimer; break;
            case 'footerNote': el.textContent = t.footerNote; break;
            case 'legalTitle': el.textContent = t.legal; break;
            case 'termsLink': el.textContent = t.terms; break;
            case 'privacyLink': el.textContent = t.privacy; break;
            case 'refundLink': el.textContent = t.refund; break;
            case 'warningLink': el.textContent = t.warning; break;
            case 'contactTitle': el.textContent = t.contact; break;
            case 'companyInfoTitle': el.textContent = t.companyInfo; break;
        }
    });

    // Update modal Close button translation
    const closeBtn = document.getElementById('modalCloseText');
    if (closeBtn) closeBtn.textContent = t.close;
}

